radius_of_sphere = float(input("Enter the radius of the sphere: "))

diameter = 2 * radius_of_sphere
circumference = 2 * 3.14 * radius_of_sphere
surface_area = 3.14 * radius_of_sphere ** 2
volume = radius_of_sphere ** 2 * 3.14 * (radius_of_sphere ** 2)

print(diameter, circumference, surface_area, volume)